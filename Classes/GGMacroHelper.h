#import <Foundation/Foundation.h>

#ifndef picrossGame_GGMacroHelper_h
#define picrossGame_GGMacroHelper_h

#define GGSquareToString(square)\
	[NSString stringWithFormat:@"x:%f y:%f w:%f h:%f", square.origin.x, square.origin.y, square.size.width, square.size.height]

#define GGSizeToString(size)\
	[NSString stringWithFormat:@"w:%f h:%f", size.width, size.height]

#define GGPointToString(point)\
	[NSString stringWithFormat:@"x:%f y:%f", point.x, point.y]
#endif
